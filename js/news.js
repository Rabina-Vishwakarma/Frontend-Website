// Load the selected page's content
window.loadPage = function(pageNumber) {
  console.log(`Loading page ${pageNumber}`);

  const container = document.getElementById("blog-container");
  container.innerHTML = ""; // Clear current blogs

  const blogsToShow = blogs[pageNumber - 1]; // Get blogs for the selected page

  blogsToShow.forEach(blog => {
      const blogBox = document.createElement("div");
      blogBox.classList.add("blog-box");

      blogBox.innerHTML = `
          <div class="blog-header">${blog.header}</div>
          <div class="blog-content">
              <div class="blog-title">
                  <a href="${blog.url}">${blog.title}</a>
                  <span class="date">${blog.date}</span>
              </div>
              <div class="comments-section">
                  <div class="username">${blog.username}</div>
                  <div class="comments-count">${blog.comments}</div>
              </div>
          </div>
      `;

      container.appendChild(blogBox);
  });

  // Update pagination active class
  const paginationLinks = document.querySelectorAll(".pagination a");
  paginationLinks.forEach(link => {
      link.classList.remove("active");
  });
  paginationLinks[pageNumber - 1].classList.add("active");
}

// Load the first page by default
loadPage(1);
const scrollToTopBtn = document.getElementById('scrollToTopBtn');

// Show the button when scrolling down
window.addEventListener('scroll', () => {
if (window.scrollY > 300) {
scrollToTopBtn.style.display = 'block';
} else {
scrollToTopBtn.style.display = 'none';
}
});

// Scroll to the top when the button is clicked
scrollToTopBtn.addEventListener('click', () => {
window.scrollTo({
top: 0,
behavior: 'smooth' // Smooth scrolling effect
});
});
